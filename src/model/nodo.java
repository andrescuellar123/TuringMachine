package model;

public class nodo {
	private String letter;
	private nodo next;
	

	/**
	 * @return the letter
	 */
	public String getLetter() {
		return letter;
	}
	/**
	 * @param letter the letter to set
	 */
	public void setLetter(String letter) {
		this.letter = letter;
	}
	/**
	 * @return the next
	 */
	public nodo getNext() {
		return next;
	}
	/**
	 * @param next the next to set
	 */
	public void setNext(nodo next) {
		this.next = next;
	}
	
	
	
	
}
